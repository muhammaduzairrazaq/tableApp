
class QuizParameters {

  static int questionNumbers=0;
  static int tableNumber=0;
  static int lowerLimit=0;
  static int upperLimit=0;

  void setQuestionNumber(int q) {
    questionNumbers = q;
  }

  void setTableNumber(int q) {
    tableNumber = q;
  }

  void setLowerLimit(int q) {
    lowerLimit = q;
  }

  void setUpperLimit(int q) {
    upperLimit = q;
  }

}